<script>
    $(document).ready(function() {
        $.ajax({
            url: "{{ route('monthly_service_cost') }}",
            type: 'GET',
            dataType: 'json',
            success: function(data) {
                createChart(data);
                console.log(data)
            }
        });


        function createChart(data) {
            // document.addEventListener("DOMContentLoaded", function() {
            // Sales Performance Chart
            var salesCtx = document.getElementById('salesPerformanceChart').getContext('2d');
            var salesPerformanceChart = new Chart(salesCtx, {
                type: 'line',
                data: {
                    labels: data.labels,
                    datasets: [{
                        label: 'Total Sales',
                        data: data.monthly_cost,
                        backgroundColor: 'rgba(75, 192, 192, 0.2)',
                        borderColor: 'rgba(75, 192, 192, 1)',
                        borderWidth: 2,
                        fill: true
                    }]
                },
                options: {
                    scales: {
                        y: {
                            beginAtZero: true
                        }
                    }
                }
            });
        }

        // Return Rate & Outstanding Payments Chart
        var returnRateCtx = document.getElementById('returnRateOutstandingPaymentsChart').getContext('2d');
        var returnRateOutstandingPaymentsChart = new Chart(returnRateCtx, {
            type: 'bar',
            data: {
                labels: ['Return Rate', 'Outstanding Payments'],
                datasets: [{
                    label: 'Percentage',
                    data: [2, 10],
                    backgroundColor: [
                        'rgba(255, 159, 64, 0.2)',
                        'rgba(255, 99, 132, 0.2)'
                    ],
                    borderColor: [
                        'rgba(255, 159, 64, 1)',
                        'rgba(255, 99, 132, 1)'
                    ],
                    borderWidth: 2
                }]
            },
            options: {
                scales: {
                    y: {
                        beginAtZero: true
                    }
                }
            }
        });

        // DataTable for Detailed Performance Data
        $('#performanceTable').DataTable({
            paging: true,
            searching: true,
            ordering: true,
            lengthChange: false,
            pageLength: 10,
            info: true,
            language: {
                searchPlaceholder: "Search records...",
                paginate: {
                    previous: "Prev",
                    next: "Next"
                },
                info: "Showing _START_ to _END_ of _TOTAL_ entries",
                infoEmpty: "No entries found",
                infoFiltered: "(filtered from _MAX_ total entries)"
            }
        });
    });
</script>

